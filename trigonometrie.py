import math

def calcul_sinus(angle):
    return math.sin(math.radians(angle))  # Convertir l'angle en radians

def calcul_cosinus(angle):
    return math.cos(math.radians(angle))  # Convertir l'angle en radians

def calcul_tangente(angle):
    return math.tan(math.radians(angle))  # Convertir l'angle en radians